/** Claude Code CLI adapter (Decision 3). */
import type { AgentEvent } from "@kiln/shared";
import type { Agent, AgentRun, AgentTask } from "./interface.js";
import { AgentCliUnavailableError, emitEvent, runCliAgent, shellQuote } from "./cli.js";

interface WritableSandbox {
  writeFile(path: string, contents: string): Promise<void>;
}

function canWriteFiles(sandbox: AgentTask["sandbox"]): sandbox is AgentTask["sandbox"] & WritableSandbox {
  return "writeFile" in sandbox && typeof (sandbox as WritableSandbox).writeFile === "function";
}

function allowLocalFallback(): boolean {
  return process.env.KILN_AGENT_FALLBACK === "1";
}

function simulateSession(prompt: string): AgentEvent[] {
  let t = 0;
  const at = (): number => (t += 8);
  const head = prompt.split("\n", 1)[0]?.slice(0, 80) ?? "task";
  return [
    { t: at(), kind: "info", text: `Claude Code session started (local fallback). Goal: ${head}` },
    { t: at(), kind: "info", text: "Reading provided context and planning approach" },
    { t: at(), kind: "command", text: "ls -la" },
    { t: at(), kind: "command", text: "cat package.json" },
    { t: at(), kind: "file", text: "Created src/index.ts" },
    { t: at(), kind: "api", text: "Initialized SDK client from context docs" },
    { t: at(), kind: "file", text: "Created src/checkout.ts" },
    { t: at(), kind: "command", text: "npm run build" },
    { t: at(), kind: "info", text: "Session complete (local fallback)" },
  ];
}

function configuredClaudeModelFlag(): string {
  const model =
    process.env.KILN_CLAUDE_MODEL ??
    process.env.ANTHROPIC_MODEL ??
    process.env.ANTHROPIC_DEFAULT_SONNET_MODEL;
  return model ? ` --model ${shellQuote(model)}` : "";
}

export class ClaudeCodeAgent implements Agent {
  readonly type = "claude-code" as const;

  async startTask(task: AgentTask): Promise<AgentRun> {
    if (!allowLocalFallback() || process.env.KILN_AGENT_USE_CLI === "1") {
      try {
        return await runCliAgent(task, {
          displayName: "Claude Code",
          binary: "claude",
          commandEnv: "KILN_CLAUDE_COMMAND",
          buildCommand: (prompt) =>
            `claude --bare -p ${shellQuote(prompt)}${configuredClaudeModelFlag()} --output-format stream-json --verbose ` +
            `--permission-mode acceptEdits --allowedTools Bash Read Edit Write MultiEdit Glob Grep LS WebFetch`,
        });
      } catch (err) {
        if (!(err instanceof AgentCliUnavailableError) || !allowLocalFallback()) throw err;
      }
    }

    const events: AgentEvent[] = [];
    for (const event of simulateSession(task.prompt)) await emitEvent(task, events, event);
    if (canWriteFiles(task.sandbox)) {
      await task.sandbox.writeFile(
        "src/index.ts",
        ["// Generated by the Claude Code local fallback.", "export const integrationStatus = 'ready';", ""].join("\n"),
      );
      await task.sandbox.writeFile(
        "src/checkout.ts",
        ["// Generated checkout scaffold.", "export const checkout = { paymentIntent: true };", ""].join("\n"),
      );
      await task.sandbox.writeFile(
        "README.md",
        `# Agent output\n\nThis local fallback created a checkout scaffold for:\n\n${task.config.task}\n`,
      );
    }
    const steps = events.filter((event) => event.kind === "command" || event.kind === "file").length;
    return {
      events,
      tokens: 1_200 * steps,
      steps,
      async collectArtifacts(): Promise<void> {},
    };
  }
}
